const { Schema, model } = require( 'mongoose' );


const UserSchema = new Schema({
  username: {
    type: String,
    required: true,
    unique: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  role: {
    type: String,
    enum: ['admin', 'client'],
    default: 'client'
  }
});

const UserModel = model( 'User', UserSchema );

module.exports = UserModel;

